'use strict';

QUnit.module('Тестируем функцию anagram', function () {
	QUnit.test('Функция работает правильно', function (assert) {
		const input = [
			'кот', 'пила', 'барокко',
			'стоп', 'ток', 'кошка',
			'липа', 'коробка', 'пост'
		];

		const output = [
			[ 'барокко', 'коробка' ],
			[ 'кот', 'ток' ],
			[ 'липа', 'пила' ],
			[ 'пост', 'стоп' ]
		];

		assert.deepEqual(anagram(input), output);
	});

	QUnit.test('Функция работает правильно', function (assert) {
		const input = null;

		const output = null;

		assert.deepEqual(anagram(input), output);
	});

	QUnit.test('Функция работает правильно', function (assert) {
		const input = 'null';

		const output = null;

		assert.deepEqual(anagram(input), output);
	});
});
